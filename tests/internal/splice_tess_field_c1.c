/* INTERNAL DEVELOPMENT TOOL -- not part of the permanent test suite, not
   registered with CTest, no exit-code contract to keep stable.

   WHAT: C1 (must_recalculate_normals=1) adaptation of splice_tess_field.c
   for the hand.stl Acrobat blank-model-tree investigation (see
   E:\Work\nanoPRC_supplementary_files\ISO-SPEC\
   hand-stl-acrobat-blanktree-investigation-22July.md for full context).
   Same technique as the original tool: take a REAL, Acrobat-working raw
   .prc file's PRC_TYPE_TESS_3D_Compressed entry and reproduce it FIELD BY
   FIELD, copying every field's raw bits verbatim from the real file EXCEPT
   for exactly one caller-selected field, which is instead regenerated
   fresh through this write facility's own production primitives, fed the
   real, decoded values for that field.

   The original tool hard-codes the C2 (must_recalculate_normals=0) field
   sequence and was built against a specific C2 test file (the hockey
   puck). This variant hard-codes the C1 sequence and field-value
   assertions matching hand-ic3d-extracted.prc specifically (the real,
   independently-produced, Acrobat-working encode of hand.stl):
   must_recalculate_normals=1, is_point_color=0, is_multiple_line_
   attribute=0, no_texture=1, has_behaviors=0. The C1-specific fields after
   must_recalculate_normals differ entirely from C2's: normal_is_reversed
   (a plain per-triangle bit array, NOT Huffman-compressed -- a different
   kind of candidate than the other five, which are all variable-bit-width/
   Huffman-compressed arrays), crease_angle, normal_recalculation_flags --
   replacing C2's normal_angle_number_of_bits/normal_binary_data_size/
   normal_binary_data/normal_angle_array/is_face_planar entirely (see
   prc_parse_tess.c ~lines 951-985 for the exact parser field order this
   mirrors, and prc_write_compress_tess.c's prc_write_compress_tess_to_
   stream for the writer side).

   HOW: usage: splice_tess_field_c1.exe <real_input.prc> <output.prc>
   [field_name]. field_name selects which single field gets regenerated by
   this write facility instead of copied verbatim; omit it (or pass
   "none") to copy every field verbatim, which should reproduce the real
   file's tessellation section bit-for-bit -- a self-check that this
   tool's field-walk is correct before trusting any non-"none" run.
   Recognized field_name values: point_array, edge_status_array,
   triangle_face_array, point_reference_array, normal_is_reversed,
   line_attribute_array.

   LIMITATIONS: hard-codes the field sequence and conditional branches
   matching hand-ic3d-extracted.prc specifically -- NOT a general C1 file
   tool. Only tests one field per run; run multiple times with different
   field_name values to bisect further. */
#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <math.h>
#include "prc_data.h"
#include "prc_api.h"
